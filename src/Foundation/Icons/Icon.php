    /**
     * @return list<string>
     */
    public static function values(): array
    {
        return array_values(array_unique(array_map(
            static fn (self $icon): string => $icon->value(),
            self::cases(),
        )));
    }

    public static function resolve(string|self $icon): string
    {
        if ($icon instanceof self) {
            return $icon->value();
        }

        return self::tryResolve($icon)?->value() ?? $icon;
    }

    public static function tryResolve(string $icon): ?self
    {
        /** @var array<string, self>|null $map */
        static $map = null;

        if ($map === null) {
            $map = [];
            foreach (self::cases() as $case) {
                // Use ??= so the first registered case wins for any given normalised key,
                // matching the original linear-scan behaviour (first match is returned).
                $map[self::normalise($case->name)] ??= $case;
                $map[self::normalise($case->value())] ??= $case;
            }
        }

        return $map[self::normalise($icon)] ?? null;
    }

    private static function toKebabCase(string $name): string
    {
        if (preg_match('/^h[1-3]$/', $name) === 1) {
            return $name;
        }

        $value = preg_replace('/(?<=\p{Lu})(\p{Lu}\p{Ll})/u', '-$1', $name) ?? $name;
        $value = preg_replace('/(?<=\p{Ll}|\p{Nd})(\p{Lu})/u', '-$1', $value) ?? $value;
        $value = preg_replace('/(?<=\p{L})(\p{Nd}+)/u', '-$1', $value) ?? $value;
        $value = preg_replace('/(?<=\p{Nd})(\p{L})/u', '-$1', $value) ?? $value;

        return strtolower($value);
    }

    private static function normalise(string $icon): string
    {
        return strtolower(preg_replace('/[^a-z0-9]+/i', '-', $icon) ?? $icon);
    }
